/*
    1.7. 예제. 문자상수(Escape Sequence)
*/
#include <stdio.h>

int main(void)
{
    printf("한줄 내림: \n");
    printf("\n");
    printf("뒤로 한칸: _\b");
    printf("\n");
    printf("탭키 추가: \t");
    printf("\n");
    printf("역슬래시 자체: \\");
    printf("\n");
    printf("작은따옴표: \' ");
    printf("\n");
    printf("큰따옴표: \" ");
    printf("\n");

    return 0;
}
