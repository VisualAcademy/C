// 참고용 소스
#include <stdio.h>

void main()
{
	printf(
		"%d\n",
		('N' - 'A' + ('N' - 'A') * 26 + ('N' - 'A') * 26 * 26) % 1000
	);//139
	printf(
		"%d\n",
		('B' - 'A' + ('B' - 'A') * 26 + ('B' - 'A') * 26 * 26) % 1000
	);//703
	printf(
		"%d\n",
		('C' - 'A' + ('C' - 'A') * 26 + ('C' - 'A') * 26 * 26) % 1000
	);//406

	printf(
		"%d\n",
		('B' - 'A' + ('B' - 'A') * 26 + ('B' - 'A') * 26 * 26) % 1000
	);//703 : B
	printf(
		"%d\n",
		('B' - 'A' + ('B' - 'A') * 26 + ('B' - 'A') * 26 * 26) % 1000
	);//703 : BB
}
